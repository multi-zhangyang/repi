/** Firmware archive inventory CAP: 7z/cpio list (no secret dump). */
export const FIRMWARE_ARCHIVE_HOST_LINES: string[] = [
	"# Archive/firmware inventory via 7z/cpio when target is archive or extracted blob",
	'if [ -f "$target" ]; then',
	'  FT=$(file -b "$target" 2>/dev/null || true)',
	'  printf "[firmware-file-type] %s\\n" "$FT"',
	"  if command -v 7z >/dev/null 2>&1; then",
	'    case "$FT" in',
	"      *Zip*|*7-zip*|*gzip*|*XZ*|*bzip2*|*tar*|*RAR*|*ISO*|*Squash*)",
	'        printf "[firmware-7z] host=1 bin=%s\\n" "$(command -v 7z)"',
	'        timeout 30s 7z l -ba "$target" 2>/dev/null | head -60 | sed "s/^/[firmware-7z] /"',
	'        ZCNT=$(timeout 30s 7z l -ba "$target" 2>/dev/null | wc -l | tr -d " ")',
	'        printf "[firmware-7z] ok=1 entries=%s\\n" "${ZCNT:-0}"',
	'        if [ "${ZCNT:-0}" -gt 0 ]; then CAP_EXTRACT=1; CAP_IMAGE=1; fi',
	"        ;;",
	'      *) printf "[firmware-7z] skip=1 note=type_not_archive\\n" ;;',
	"    esac",
	"  else",
	'    printf "[firmware-7z] host=0 note=7z_missing\\n"',
	"  fi",
	'  if command -v cpio >/dev/null 2>&1 && echo "$FT" | grep -qiE "cpio|initramfs|ASCII cpio"; then',
	'    printf "[firmware-cpio] host=1 bin=%s\\n" "$(command -v cpio)"',
	'    CPIO_LIST=$(timeout 20s cpio -it < "$target" 2>/dev/null | head -40 || true)',
	'    if [ -n "$CPIO_LIST" ]; then printf "%s\\n" "$CPIO_LIST" | sed "s/^/[firmware-cpio] /"; printf "[firmware-cpio] ok=1\\n"; CAP_EXTRACT=1; CAP_IMAGE=1; else printf "[firmware-cpio] ok=0 note=empty-list\\n"; fi',
	"  fi",
	"  # strings credential surface on image",
	"  if command -v strings >/dev/null 2>&1; then",
	'    strings -a -n 6 "$target" 2>/dev/null | grep -iE "passwd|shadow|dropbear|busybox|httpd|telnet|root:|password=|admin:" | head -40 | sed "s/^/[firmware-strings-cred] /" || true',
	"  fi",
	"fi",
] as const;
