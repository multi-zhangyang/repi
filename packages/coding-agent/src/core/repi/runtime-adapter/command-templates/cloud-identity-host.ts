/** Host cloud/identity CAP: curl IMDS status + docker.sock + env/files (no secret dump). */
export const CLOUD_IDENTITY_HOST_LINES = [
	"if command -v curl >/dev/null 2>&1 || command -v printenv >/dev/null 2>&1 || [ -S /var/run/docker.sock ]; then",
	'  printf "[cloud-host] curl=%s jq=%s docker_sock=%s\\n" "$(command -v curl || true)" "$(command -v jq || true)" "$([ -S /var/run/docker.sock ] && echo 1 || echo 0)"',
	"  # Safe IMDS HTTP status probes (no credential/token dump; short timeouts)",
	"  # REPI_IMDS_BASE_URL rewrites link-local IMDS hosts to a local fixture (cloud smoke).",
	"  if command -v curl >/dev/null 2>&1; then",
	'    IMDS_BASE="${REPI_IMDS_BASE_URL:-}"',
	'    for item in "aws-imds|http://169.254.169.254/latest/meta-data/|" "aws-imds-iam|http://169.254.169.254/latest/meta-data/iam/security-credentials/|" "azure-imds|http://169.254.169.254/metadata/instance?api-version=2021-02-01|Metadata:true" "gcp-metadata|http://metadata.google.internal/computeMetadata/v1/|Metadata-Flavor:Google"; do',
	'      label="${item%%|*}"; rest="${item#*|}"; url="${rest%%|*}"; hdr="${rest#*|}"; hdr="${hdr%%|*}";',
	'      if [ -n "$IMDS_BASE" ]; then',
	'        case "$label" in',
	'          aws-imds) url="${IMDS_BASE%/}/latest/meta-data/" ;;',
	'          aws-imds-iam) url="${IMDS_BASE%/}/latest/meta-data/iam/security-credentials/" ;;',
	'          azure-imds) url="${IMDS_BASE%/}/metadata/instance?api-version=2021-02-01" ;;',
	'          gcp-metadata) url="${IMDS_BASE%/}/computeMetadata/v1/" ;;',
	"        esac",
	'        printf "[cloud-imds-http] mock_base=%s label=%s\\n" "$IMDS_BASE" "$label"',
	"      fi",
	'      if [ -n "$hdr" ]; then',
	'        code=$(curl -s -o /dev/null -w "%{http_code}" --connect-timeout 1 --max-time 2 -H "$hdr" "$url" 2>/dev/null || echo 000)',
	"      else",
	'        code=$(curl -s -o /dev/null -w "%{http_code}" --connect-timeout 1 --max-time 2 "$url" 2>/dev/null || echo 000)',
	"      fi",
	'      printf "[cloud-imds-http] label=%s url=%s http=%s\\n" "$label" "$url" "${code:-000}"',
	'      case "${code:-000}" in 200|401|403|404|405) CAP_META=1 ;; esac',
	'      if [ "${code:-000}" = "200" ]; then CAP_IDENTITY=1; fi',
	'      if [ -n "$IMDS_BASE" ] && [ "${code:-000}" = "200" ]; then CAP_IMDS_MOCK=1; fi',
	'      if [ -z "$IMDS_BASE" ] && [ "${code:-000}" != "000" ]; then CAP_IMDS_SCAFFOLD=1; fi',
	"    done",
	'    if [ "${CAP_IMDS_MOCK:-0}" = "1" ]; then',
	'      printf "[cloud-imds-http] ok=1 mock=1\\n"',
	'      printf "summary.cloud_imds_mock=1\\n"',
	"      # IMDSv2 token hop (fixture): PUT token then GET meta with header — no secret dump",
	'      TOK=$(curl -s -X PUT -H "X-aws-ec2-metadata-token-ttl-seconds: 21600" --connect-timeout 1 --max-time 2 "${IMDS_BASE%/}/latest/api/token" 2>/dev/null | tr -d "\\r" | head -1)',
	'      if [ -n "$TOK" ]; then',
	'        printf "[cloud-imdsv2] token_len=%s fixture=1\\n" "${#TOK}"',
	'        code2=$(curl -s -o /dev/null -w "%{http_code}" --connect-timeout 1 --max-time 2 -H "X-aws-ec2-metadata-token: $TOK" "${IMDS_BASE%/}/latest/meta-data/" 2>/dev/null || echo 000)',
	'        printf "[cloud-imdsv2] meta_http=%s\\n" "${code2:-000}"',
	'        if [ "${code2:-000}" = "200" ]; then CAP_IMDSV2=1; printf "[cloud-imdsv2] ok=1\\n"; printf "summary.cloud_imdsv2=1\\n"; fi',
	"      else",
	'        printf "[cloud-imdsv2] ok=0 note=token_empty\\n"',
	"      fi",
	'    elif [ "${CAP_META:-0}" = "1" ] && [ -z "$IMDS_BASE" ]; then',
	"      CAP_IMDS_SCAFFOLD=1",
	'      printf "[cloud-imds-http] ok=1 scaffold=1\\n"',
	"    fi",
	"  fi",
	"  # docker.sock presence/version (no privileged ops)",
	"  if [ -S /var/run/docker.sock ] && command -v curl >/dev/null 2>&1; then",
	'    DCODE=$(curl -s -o /tmp/repi-docker-ver.json -w "%{http_code}" --unix-socket /var/run/docker.sock --connect-timeout 1 --max-time 2 http://localhost/version 2>/dev/null || echo 000)',
	'    printf "[cloud-docker-host] sock=1 http=%s\\n" "${DCODE:-000}"',
	"    if [ -f /tmp/repi-docker-ver.json ] && command -v jq >/dev/null 2>&1; then",
	'      jq -r \'"[cloud-docker-host] version=" + (.Version // .version // "unknown") + " api=" + (.ApiVersion // .apiVersion // "?")\' /tmp/repi-docker-ver.json 2>/dev/null | head -3',
	"    elif [ -f /tmp/repi-docker-ver.json ]; then",
	'      head -c 200 /tmp/repi-docker-ver.json 2>/dev/null | tr "\\n" " " | sed "s/^/[cloud-docker-host] body=/"',
	'      printf "\\n"',
	"    fi",
	"    CAP_RUNTIME=1",
	"  elif [ -S /var/run/docker.sock ]; then",
	'    printf "[cloud-docker-host] sock=1 curl=0\\n"',
	"    CAP_RUNTIME=1",
	"  else",
	'    printf "[cloud-docker-host] sock=0\\n"',
	"  fi",
	"  # host env identity keys (names only / redacted)",
	"  if command -v printenv >/dev/null 2>&1; then",
	'    printenv 2>/dev/null | grep -E "^(AWS_|AZURE_|GOOGLE_|KUBE|KUBERNETES|CLOUD|ECS_|DOCKER)" | head -40 | while IFS= read -r line; do',
	'      key="${line%%=*}"; val="${line#*=}"',
	'      if echo "$key" | grep -qiE "SECRET|TOKEN|PASSWORD|KEY|CREDENTIAL"; then',
	'        printf "[cloud-host-env] key=%s redacted=1 len=%s\\n" "$key" "${#val}"',
	"      else",
	'        printf "[cloud-host-env] key=%s set=%s\\n" "$key" "$([ -n "$val" ] && echo 1 || echo 0)"',
	"      fi",
	"    done",
	"    CAP_IDENTITY=1",
	"  fi",
	"  # local aws/kube config presence via shell (no secret dump)",
	'  for p in "$HOME/.aws/credentials" "$HOME/.aws/config" "$HOME/.kube/config" "$KUBECONFIG"; do',
	'    [ -n "$p" ] || continue',
	'    if [ -f "$p" ]; then printf "[cloud-host-file] path=%s present=1 bytes=%s\\n" "$p" "$(wc -c < "$p" 2>/dev/null | tr -d " ")"; CAP_AWS_FILE=1; CAP_IDENTITY=1; else printf "[cloud-host-file] path=%s present=0\\n" "$p"; fi',
	"  done",
	"  # docker containers inventory (names/images only; no secrets)",
	"  if [ -S /var/run/docker.sock ] && command -v curl >/dev/null 2>&1; then",
	'    CCODE=$(curl -s -o /tmp/repi-docker-containers.json -w "%{http_code}" --unix-socket /var/run/docker.sock --connect-timeout 1 --max-time 3 "http://localhost/containers/json?all=1" 2>/dev/null || echo 000)',
	'    printf "[cloud-docker-containers] http=%s\\n" "${CCODE:-000}"',
	'    if [ "${CCODE:-000}" = "200" ] && [ -f /tmp/repi-docker-containers.json ]; then',
	"      if command -v jq >/dev/null 2>&1; then",
	'        jq -r \'.[] | "[cloud-docker-containers] id=" + (.Id[0:12] // "?") + " image=" + ((.Image // "?")|tostring) + " names=" + ((.Names // [])|join(","))\' /tmp/repi-docker-containers.json 2>/dev/null | head -20',
	"        CNT=$(jq 'length' /tmp/repi-docker-containers.json 2>/dev/null || echo 0)",
	'        printf "[cloud-docker-containers] ok=1 count=%s\\n" "${CNT:-0}"',
	"      else",
	'        head -c 300 /tmp/repi-docker-containers.json 2>/dev/null | tr "\\n" " " | sed "s/^/[cloud-docker-containers] body=/"',
	'        printf "\\n[cloud-docker-containers] ok=1 count=unknown\\n"',
	"      fi",
	"      CAP_RUNTIME=1",
	"    else",
	'      printf "[cloud-docker-containers] ok=0 http=%s\\n" "${CCODE:-000}"',
	"    fi",
	"  fi",
	'  printf "[cloud-host] ok=1\\n"',
	"else",
	'  printf "[cloud-host] ok=0 note=curl_printenv_missing\\n"',
	"fi",
] as const;
