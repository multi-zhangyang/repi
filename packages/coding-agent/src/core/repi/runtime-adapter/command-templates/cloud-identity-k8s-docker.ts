export const CLOUD_IDENTITY_K8S_DOCKER_LINES: string[] = [
	"# Local k8s serviceaccount + docker images inventory (no secret dump)",
	'SA_ROOT=""',
	'if [ -n "${REPI_K8S_SA_DIR:-}" ] && [ -d "$REPI_K8S_SA_DIR" ]; then',
	'  SA_ROOT="$REPI_K8S_SA_DIR"',
	"elif [ -d /tmp/repi-k8s-sa ]; then",
	"  SA_ROOT=/tmp/repi-k8s-sa",
	"elif [ -d /var/run/secrets/kubernetes.io/serviceaccount ]; then",
	"  SA_ROOT=/var/run/secrets/kubernetes.io/serviceaccount",
	"elif [ -d /run/secrets/kubernetes.io/serviceaccount ]; then",
	"  SA_ROOT=/run/secrets/kubernetes.io/serviceaccount",
	"fi",
	'if [ -n "$SA_ROOT" ] && [ -d "$SA_ROOT" ]; then',
	'  printf "[cloud-k8s-sa] present=1 root=%s\\n" "$SA_ROOT"',
	"  for f in namespace ca.crt service-ca.crt token; do",
	'    if [ -f "$SA_ROOT/$f" ]; then',
	'      if [ "$f" = "token" ]; then',
	'        printf "[cloud-k8s-sa] file=%s present=1 bytes=%s redacted=1\\n" "$f" "$(wc -c < "$SA_ROOT/$f" 2>/dev/null | tr -d " ")"',
	'      elif [ "$f" = "namespace" ]; then',
	'        printf "[cloud-k8s-sa] namespace=%s\\n" "$(tr -d "\\n" < "$SA_ROOT/$f" 2>/dev/null)"',
	"      else",
	'        printf "[cloud-k8s-sa] file=%s present=1 bytes=%s\\n" "$f" "$(wc -c < "$SA_ROOT/$f" 2>/dev/null | tr -d " ")"',
	"      fi",
	"      CAP_K8S=1; CAP_IDENTITY=1",
	"    else",
	'      printf "[cloud-k8s-sa] file=%s present=0\\n" "$f"',
	"    fi",
	"  done",
	'  printf "[cloud-k8s-sa] ok=1\\n"',
	'  printf "summary.cloud_k8s_sa=1\\n"',
	"else",
	'  printf "[cloud-k8s-sa] present=0\\n"',
	'  printf "[cloud-k8s-sa] scaffold=1 next=check /var/run/secrets/kubernetes.io/serviceaccount_or_set_REPI_K8S_SA_DIR\\n"',
	"  CAP_K8S=1",
	"fi",
	"if [ -S /var/run/docker.sock ] && command -v curl >/dev/null 2>&1; then",
	'  ICODE=$(curl -s -o /tmp/repi-docker-images.json -w "%{http_code}" --unix-socket /var/run/docker.sock --connect-timeout 1 --max-time 3 "http://localhost/images/json" 2>/dev/null || echo 000)',
	'  printf "[cloud-docker-images] http=%s\\n" "${ICODE:-000}"',
	'  if [ "${ICODE:-000}" = "200" ] && [ -f /tmp/repi-docker-images.json ]; then',
	"    if command -v jq >/dev/null 2>&1; then",
	'      jq -r \'.[:15][] | "[cloud-docker-images] id=" + ((.Id // "?")|tostring|.[0:12]) + " tags=" + ((.RepoTags // [])|join(","))\' /tmp/repi-docker-images.json 2>/dev/null | head -20',
	"      ICNT=$(jq 'length' /tmp/repi-docker-images.json 2>/dev/null || echo 0)",
	'      printf "[cloud-docker-images] ok=1 count=%s\\n" "${ICNT:-0}"',
	"    else",
	'      printf "[cloud-docker-images] ok=1 count=unknown\\n"',
	"    fi",
	"    CAP_RUNTIME=1",
	"  else",
	'    printf "[cloud-docker-images] ok=0 http=%s\\n" "${ICODE:-000}"',
	"  fi",
	"fi",
] as const;
