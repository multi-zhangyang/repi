/** DFIR pure-host deepen via tcpdump when tshark missing. */
export const DFIR_TCPDUMP_DEEP_LINES = [
	'if command -v tcpdump >/dev/null 2>&1 && [ -n "${1:-}" ] && [ -e "${1:-}" ]; then',
	"python3 - \"$1\" <<'PY'",
	"import collections, re, subprocess, sys",
	"path = sys.argv[1]",
	"try:",
	"  proc = subprocess.run(['tcpdump','-nn','-r',path,'-c','200'], capture_output=True, text=True, timeout=8)",
	"  text = (proc.stdout or '') + '\\n' + (proc.stderr or '')",
	"except Exception as exc:",
	"  print('[dfir-tcpdump-deep] failed=%s' % type(exc).__name__); text = ''",
	"flows = collections.Counter()",
	"ports = collections.Counter()",
	"protos = collections.Counter()",
	"http = 0; dns = 0; tls = 0",
	"for line in text.splitlines():",
	"  if ' IP ' in line or re.search(r'\\bIP6?\\b', line):",
	"    protos['ip'] += 1",
	"  if ' TCP ' in line or 'Flags [' in line: protos['tcp'] += 1",
	"  if ' UDP ' in line: protos['udp'] += 1",
	"  if ' ICMP ' in line: protos['icmp'] += 1",
	"  m = re.search(r'([\\d.]+)\\.(\\d+)\\s*>\\s*([\\d.]+)\\.(\\d+)', line)",
	"  if m:",
	"    a,ap,b,bp = m.groups()",
	"    key = '%s:%s <-> %s:%s' % (a,ap,b,bp) if a < b else '%s:%s <-> %s:%s' % (b,bp,a,ap)",
	"    flows[key] += 1",
	"    ports[ap] += 1; ports[bp] += 1",
	"  if re.search(r'HTTP|GET |POST |Host:', line): http += 1",
	"  if re.search(r'\\b53\\b|DNS|A\\?|AAAA\\?', line): dns += 1",
	"  if re.search(r'\\b443\\b|TLS|Client Hello|Server Hello', line): tls += 1",
	"print('[dfir-tcpdump-deep] lines=%d flows=%d httpish=%d dnsish=%d tlsish=%d' % (len([l for l in text.splitlines() if l.strip()]), len(flows), http, dns, tls))",
	"for k,v in flows.most_common(12):",
	"  print('[dfir-tcpdump-flow] %s packets=%d' % (k, v))",
	"for p,v in ports.most_common(10):",
	"  print('[dfir-tcpdump-port] port=%s hits=%d' % (p, v))",
	"for proto,v in protos.most_common():",
	"  print('[dfir-tcpdump-proto] name=%s hits=%d' % (proto, v))",
	"if flows or protos: print('[dfir-tcpdump-deep] ok=1')",
	"PY",
	"  CAP_TCPDUMP=1",
	"fi",
] as const;
