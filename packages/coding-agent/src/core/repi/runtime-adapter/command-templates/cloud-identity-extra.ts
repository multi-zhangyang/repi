	"    s=socket.create_connection((host, port), timeout=0.35)",
	"    s.close()",
	"    print('[cloud-imds-reach] label=%s host=%s reachable=1' % (label, host))",
	"  except Exception as exc:",
	"    print('[cloud-imds-reach] label=%s host=%s reachable=0 err=%s' % (label, host, type(exc).__name__))",
