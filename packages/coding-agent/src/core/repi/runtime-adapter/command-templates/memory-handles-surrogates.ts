export const MEMORY_HANDLES_SURROGATE_LINES = [
	"python3 - \"$target\" <<'PY'",
	"import collections, pathlib, re, sys",
	"p = pathlib.Path(sys.argv[1])",
	"data = p.read_bytes()[:16_000_000] if p.is_file() else b''",
	"text = data.decode('latin1', 'ignore')",
	"print('[mem-handles] path=%s bytes=%d pure_python=1' % (p, len(data)))",
	"reg = collections.Counter()",
	"# single-backslash registry paths as often appear in dumps/strings",
	"for m in re.finditer(r'(?:HKEY_[A-Z_]+|HKLM|HKCU|HKCR)(?:\\\\[\\w .\\-]+)+', text, re.I):",
	"  key = m.group(0)[:160]",
	"  reg[key.lower()] += 1",
	"for k, hits in reg.most_common(16):",
	"  print('[mem-registry] key=%s hits=%d' % (k, hits))",
	"print('[mem-registry] unique=%d' % len(reg))",
	"if reg: print('summary.mem_registry=1')",
	"envs = []",
	"for m in re.finditer(r'\\b(PATH|TEMP|TMP|APPDATA|LOCALAPPDATA|USERPROFILE|HOME|SHELL)=([^\\x00\\s]{1,200})', text):",
	"  envs.append((m.group(1), m.group(2)[:160]))",
	"for name, val in envs[:12]:",
	"  print('[mem-env] name=%s value=%s' % (name, val))",
	"print('[mem-env] hits=%d' % len(envs))",
	"if envs: print('summary.mem_env=1')",
	"paths = collections.Counter()",
	"for m in re.finditer(r'(?:[A-Za-z]:\\\\[\\w .\\-\\\\]+|/(?:home|tmp|var|etc|usr|opt)/[\\w./\\-]{2,120})', text):",
	"  paths[m.group(0)[:160]] += 1",
	"for path, hits in paths.most_common(16):",
	"  print('[mem-handles] path=%s hits=%d' % (path, hits))",
	"print('[mem-handles] unique=%d pure_python=1' % len(paths))",
	"if paths: print('summary.mem_handles=1')",
	"print('[mem-handles] ok=1 registry=%d env=%d paths=%d' % (len(reg), len(envs), len(paths)))",
	"PY",
] as const;
