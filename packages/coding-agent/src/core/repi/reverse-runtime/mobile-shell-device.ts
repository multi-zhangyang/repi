	'SDK_ROOT="${ANDROID_SDK_ROOT:-${ANDROID_HOME:-}}"',
	'if [ -z "$SDK_ROOT" ]; then',
	'  for cand in "$HOME/Android/Sdk" /usr/lib/android-sdk /opt/android-sdk /opt/android-sdk-linux /usr/local/android-sdk; do',
