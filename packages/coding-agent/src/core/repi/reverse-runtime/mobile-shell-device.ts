	'  if [ -d "$SI" ]; then find "$SI" -maxdepth 3 -type d 2>/dev/null | head -20 | sed "s/^/[mobile-sdk-system-image] /" || true; SI_N=$(find "$SI" -maxdepth 4 -type d -name \'*/\' 2>/dev/null | wc -l | tr -d " "); printf "[mobile-sdk-system-images] present=1\\n"; fi',
	'  if [ -x "$PT/adb" ]; then printf "[mobile-sdk] adb_bin=%s\\n" "$PT/adb"; fi',
	'  if [ -x "$EMU/emulator" ]; then printf "[mobile-sdk] emulator_bin=%s\\n" "$EMU/emulator"; fi',
	'  if command -v sdkmanager >/dev/null 2>&1; then printf "[mobile-sdk] sdkmanager=%s\\n" "$(command -v sdkmanager)"; elif [ -x "$CT/latest/bin/sdkmanager" ]; then printf "[mobile-sdk] sdkmanager=%s\\n" "$CT/latest/bin/sdkmanager"; fi',
	'  if command -v avdmanager >/dev/null 2>&1; then printf "[mobile-sdk] avdmanager=%s\\n" "$(command -v avdmanager)"; elif [ -x "$CT/latest/bin/avdmanager" ]; then printf "[mobile-sdk] avdmanager=%s\\n" "$CT/latest/bin/avdmanager"; fi',
	'  if [ -d "$AVD_HOME" ]; then',
	'    AVD_N=$(find "$AVD_HOME" -maxdepth 1 -type d -name \'*.avd\' 2>/dev/null | wc -l | tr -d " ")',
	'    printf "[mobile-sdk-avd-home] path=%s count=%s\\n" "$AVD_HOME" "${AVD_N:-0}"',
	'    find "$AVD_HOME" -maxdepth 1 -type d -name \'*.avd\' 2>/dev/null | head -12 | sed "s/^/[mobile-sdk-avd] /" || true',
	"  else",
	'    printf "[mobile-sdk-avd-home] path=%s present=0\\n" "$AVD_HOME"',
	"  fi",
	"  # extend PATH for this shell so later adb/emulator discovery sees SDK tools",
	'  export PATH="$PT:$EMU:$CT/latest/bin:$PATH"',
