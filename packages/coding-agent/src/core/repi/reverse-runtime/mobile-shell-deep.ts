	"        pass",
	"    for m in re.finditer(r'\b(?:com|org|net).[a-zA-Z0-9_]+(?:.[a-zA-Z0-9_]+)+\b', text):",
	"        print('[mobile-package-candidate] name=%s source=text' % m.group(0)); hits_pkg += 1",
	"print('[mobile-apk-deep] ok=1 ssl_members=%d root_members=%d package_hits=%d' % (hits_ssl, hits_root, hits_pkg))",
	"if hits_pkg > 0: print('summary.mobile_package=1')",
	"if hits_ssl > 0: print('summary.mobile_ssl=1')",
	"blob=b''.join(zf.read(n)[:40000] for n in list(names)[:60])",
	"if any(x in blob for x in (b'android.intent.action.VIEW',b'BROWSABLE',b'repi://',b'android:exported')):",
	"    print('[mobile-deeplink] ok=1 pure_python=1 source=zip_strings'); print('summary.mobile_deeplink=1')",
	"    (b'repi://' in blob) and print('[mobile-deeplink] scheme=repi pure_python=1')",
	"if b'exported' in blob.lower() or b'android:exported' in blob:",
	"    print('[mobile-exported] hits=1 pure_python=1 source=zip_strings'); print('summary.mobile_exported=1')",
	"zf.close()",
	"PY",
	"fi",
