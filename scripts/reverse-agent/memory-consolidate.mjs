#!/usr/bin/env node
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { homedir } from "node:os";
import { dirname, join, resolve } from "node:path";

const root = resolve(process.argv[2] && !process.argv[2].startsWith("--") ? process.argv[2] : process.cwd());
const dryRun = process.argv.includes("--dry-run");
const json = process.argv.includes("--json");
const agentDir = process.env.REPI_CODING_AGENT_DIR || process.env.REPI_AGENT_DIR || join(homedir(), ".repi", "agent");
const memoryDir = join(agentDir, "recon", "memory");
const eventsPath = join(memoryDir, "events.jsonl");
const reportPath = join(memoryDir, "consolidation-report.json");
const targets = {
	project: join(memoryDir, "project-memory.md"),
	procedural: join(memoryDir, "procedural-memory.md"),
};

function readJsonl(path) {
	try {
		return readFileSync(path, "utf8").split(/\r?\n/).flatMap((line) => {
			if (!line.trim()) return [];
			try { return [JSON.parse(line)]; } catch { return []; }
		});
	} catch {
		return [];
	}
}

function redact(text) {
	return String(text ?? "")
		.replace(/\bsk-[A-Za-z0-9_-]{8,}\b/g, "<redacted:api-key>")
		.replace(/\bghp_[A-Za-z0-9_]{16,}\b/g, "<redacted:github-token>")
		.replace(/\bgithub_pat_[A-Za-z0-9_]{16,}\b/g, "<redacted:github-token>")
		.replace(/(?:AUTH_TOKEN|API_KEY|PASSWORD|SECRET)=\S+/gi, (m) => `${m.split("=")[0]}=<redacted>`)
		.replace(/\s+/g, " ")
		.trim();
}

function scoreEvent(event) {
	let score = 0;
	if (event.outcome === "success") score += 35;
	if (event.outcome === "partial" || event.outcome === "repair") score += 18;
	if (event.quality?.replayVerified) score += 25;
	if (event.promotion?.playbookCandidate) score += 16;
	if ((event.commands ?? []).length) score += 12;
	if ((event.reuseRules ?? []).length) score += 10;
	if ((event.lessons ?? []).length) score += 8;
	score += Math.round((event.quality?.confidence ?? 0) * 20);
	if (event.outcome === "failure" || event.outcome === "blocked") score -= 15;
	return score;
}

function unique(values, limit) {
	const out = [];
	const seen = new Set();
	for (const value of values) {
		const text = redact(value);
		if (!text || text.length < 6) continue;
		const key = text.toLowerCase();
		if (seen.has(key)) continue;
		seen.add(key);
		out.push(text);
		if (out.length >= limit) break;
	}
	return out;
}

function generatedBlock(title, lines) {
	return [
		`<!-- REPI:${title}:BEGIN -->`,
		`_Generated by \`repi memory consolidate\` at ${new Date().toISOString()}._`,
		"",
		...(lines.length ? lines : ["- none yet"]),
		`<!-- REPI:${title}:END -->`,
		"",
	].join("\n");
}

function replaceBlock(existing, title, block, header) {
	const marker = new RegExp(`<!-- REPI:${title}:BEGIN -->[\\s\\S]*?<!-- REPI:${title}:END -->\\n?`, "m");
	const base = existing.trim() ? existing.replace(marker, "").trimEnd() : header.trimEnd();
	return `${base}\n\n${block}`;
}

const events = readJsonl(eventsPath).filter((event) => event && event.kind === "repi-memory-event");
const high = events
	.map((event) => ({ event, score: scoreEvent(event) }))
	.filter((row) => row.score >= 45)
	.sort((a, b) => b.score - a.score || String(b.event.ts).localeCompare(String(a.event.ts)))
	.slice(0, 24);

const commandLines = unique(high.flatMap(({ event }) => event.commands ?? []), 24).map((cmd) => `- \`${cmd.replace(/`/g, "\\`")}\``);
const ruleLines = unique(high.flatMap(({ event }) => event.reuseRules ?? []), 24).map((rule) => `- ${rule}`);
const lessonLines = unique(high.flatMap(({ event }) => event.lessons ?? []), 24).map((lesson) => `- ${lesson}`);
const caseLines = high.slice(0, 12).map(({ event, score }) => `- id=${event.id} score=${score} route=${redact(event.route)} target=${redact(event.target ?? "workspace")} outcome=${event.outcome}`);

mkdirSync(memoryDir, { recursive: true });
const projectHeader = "# REPI Project Memory\n\n当前 workspace 的构建、运行、测试、入口、常用命令写在这里。\n";
const proceduralHeader = "# REPI Procedural Memory\n\n可复用 workflow / checklist / verified command template 写在这里。\n";
const projectExisting = existsSync(targets.project) ? readFileSync(targets.project, "utf8") : projectHeader;
const proceduralExisting = existsSync(targets.procedural) ? readFileSync(targets.procedural, "utf8") : proceduralHeader;
const nextProject = replaceBlock(
	projectExisting,
	"PROJECT-CONSOLIDATED",
	generatedBlock("PROJECT-CONSOLIDATED", ["## High-value commands", ...commandLines, "", "## Recent high-score cases", ...caseLines]),
	projectHeader,
);
const nextProcedural = replaceBlock(
	proceduralExisting,
	"PROCEDURAL-CONSOLIDATED",
	generatedBlock("PROCEDURAL-CONSOLIDATED", ["## Reuse rules", ...ruleLines, "", "## Lessons", ...lessonLines]),
	proceduralHeader,
);

const report = {
	kind: "repi-memory-consolidation-report",
	schemaVersion: 1,
	generatedAt: new Date().toISOString(),
	root,
	agentDir,
	eventsPath,
	eventCount: events.length,
	selectedCount: high.length,
	projectMemoryPath: targets.project,
	proceduralMemoryPath: targets.procedural,
	dryRun,
	selected: high.map(({ event, score }) => ({ id: event.id, score, route: event.route, target: event.target, outcome: event.outcome, commands: (event.commands ?? []).slice(0, 3) })),
};

if (!dryRun) {
	writeFileSync(targets.project, nextProject, "utf8");
	writeFileSync(targets.procedural, nextProcedural, "utf8");
	writeFileSync(reportPath, `${JSON.stringify(report, null, 2)}\n`, "utf8");
}

if (json) console.log(JSON.stringify(report, null, 2));
else {
	console.log("REPI Memory Consolidation");
	console.log(`events=${events.length} selected=${high.length} dryRun=${dryRun}`);
	console.log(`project=${targets.project}`);
	console.log(`procedural=${targets.procedural}`);
	console.log(`report=${dryRun ? "<dry-run>" : reportPath}`);
}
